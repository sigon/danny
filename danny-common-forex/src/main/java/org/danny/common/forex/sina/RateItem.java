package org.danny.common.forex.sina;

public class RateItem {

	private String symbol;
	private String bank;
	private String name;
	private String xh_buy;
	private String xh_sell;
	private String xc_buy;
	private String xc_sell;
	private String mid;
	private String udpate;
	private String udate;
	private String utime;

	public String getSymbol() {
		return symbol;
	}

	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}

	public String getBank() {
		return bank;
	}

	public void setBank(String bank) {
		this.bank = bank;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getXh_buy() {
		return xh_buy;
	}

	public void setXh_buy(String xh_buy) {
		this.xh_buy = xh_buy;
	}

	public String getXh_sell() {
		return xh_sell;
	}

	public void setXh_sell(String xh_sell) {
		this.xh_sell = xh_sell;
	}

	public String getXc_buy() {
		return xc_buy;
	}

	public void setXc_buy(String xc_buy) {
		this.xc_buy = xc_buy;
	}

	public String getXc_sell() {
		return xc_sell;
	}

	public void setXc_sell(String xc_sell) {
		this.xc_sell = xc_sell;
	}

	public String getMid() {
		return mid;
	}

	public void setMid(String mid) {
		this.mid = mid;
	}

	public String getUdpate() {
		return udpate;
	}

	public void setUdpate(String udpate) {
		this.udpate = udpate;
	}

	public String getUtime() {
		return utime;
	}

	public void setUtime(String utime) {
		this.utime = utime;
	}

	public String getUdate() {
		return udate;
	}

	public void setUdate(String udate) {
		this.udate = udate;
	}
}
